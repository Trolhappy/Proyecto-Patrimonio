def create_button(parent, text, x, y, width=10, height=2, command=None, image_path=None):
    if image_path:
        from PIL import Image, ImageTk
        img = Image.open(image_path)
        img = img.resize((40, 40))
        img = ImageTk.PhotoImage(img)
        btn = tk.Button(parent, text=text, width=width, height=height,
                        command=command, compound="top", image=img)
        btn.image = img  # evitar garbage collector
    else:
        btn = tk.Button(parent, text=text, width=width, height=height, command=command)

    btn.place(x=x, y=y)
    return btn

